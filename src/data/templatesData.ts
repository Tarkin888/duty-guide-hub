// Helper functions
export const getTemplatesByCategory = (category: TemplateCategory): Template[] => {
  return templates.filter(t => t.category === category);
};

export const getTemplateById = (id: string): Template | undefined => {
  return templates.find(t => t.id === id);
};

export const searchTemplates = (query: string): Template[] => {
  const lowerQuery = query.toLowerCase();
  return templates.filter(t => 
    t.name.toLowerCase().includes(lowerQuery) ||
    t.description.toLowerCase().includes(lowerQuery) ||
    t.keywords.some(k => k.toLowerCase().includes(lowerQuery)) ||
    t.moduleReference.toLowerCase().includes(lowerQuery)
  );
};

export const getCategoryCount = (): Record<TemplateCategory, number> => {
  const counts: Record<TemplateCategory, number> = {} as Record<TemplateCategory, number>;
  Object.keys(categoryLabels).forEach(cat => {
    counts[cat as TemplateCategory] = templates.filter(t => t.category === cat).length;
  });
  return counts;
};

export const getFileTypeCounts = (): Record<FileType, number> => {
  return {
    xlsx: templates.filter(t => t.fileType === 'xlsx').length,
    docx: templates.filter(t => t.fileType === 'docx').length,
    pptx: templates.filter(t => t.fileType === 'pptx').length,
    pdf: templates.filter(t => t.fileType === 'pdf').length,
  };
};
