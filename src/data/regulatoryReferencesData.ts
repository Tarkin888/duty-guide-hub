// Helper functions
export const getReferencesByType = (type: ReferenceType): RegulatoryReference[] => {
  return regulatoryReferences.filter(ref => ref.type === type);
};

export const getReferencesByOutcome = (outcome: OutcomeCategory): RegulatoryReference[] => {
  if (outcome === 'all') return regulatoryReferences;
  return regulatoryReferences.filter(ref => ref.outcomes.includes(outcome));
};

export const getReferencesCount = () => ({
  total: regulatoryReferences.length,
  handbook: getReferencesByType('handbook').length,
  multiFilmReviews: getReferencesByType('multi-firm-review').length,
  enforcement: getReferencesByType('enforcement').length,
});

export const searchReferences = (query: string): RegulatoryReference[] => {
  const lowerQuery = query.toLowerCase();
  return regulatoryReferences.filter(ref => 
    ref.title.toLowerCase().includes(lowerQuery) ||
    ref.fullTitle.toLowerCase().includes(lowerQuery) ||
    ref.reference.toLowerCase().includes(lowerQuery) ||
    ref.summary.toLowerCase().includes(lowerQuery) ||
    ref.keywords.some(k => k.toLowerCase().includes(lowerQuery)) ||
    ref.keyTopics.some(t => t.toLowerCase().includes(lowerQuery))
  );
};
